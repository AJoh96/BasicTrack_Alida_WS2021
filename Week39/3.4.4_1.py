while True:
    day_number = float(input("What day number is it?"))
    if day_number == 0:
        print("Sunday")
    elif day_number == 1:
        print("Monday")
    elif day_number == 2:
        print("Tuesday")
    elif day_number == 3:
        print("Wednesday")
    elif day_number == 4:
        print("Thursday")
    elif day_number == 5:
        print("Friday")
    elif day_number == 6:
        print("Saturday")
    else:
        break
